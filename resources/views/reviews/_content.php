    <h3>Ambra Vidal</h3>
    <p>¿Para qué es necesaria la presencia de Ambra Vidal como compañera de aventuras de Langdon? ¿Solo para que en la peli haya una mujer “hermosa”?</p>

    <h3>El Sicario</h3>
    <p>Mención aparte para el personaje del "sicario" de turno, que en este libro es un almirante (vestido de almirante) que <strong>mata con un rosario convertible en pistola y hecho con una impresora 3D</strong>, de ideología carlista (¡canta la Marcha de Oriamendi!), y que actúa a las órdenes del antipapa del Palmar de Troya, todo muy lógico y creíble.</p>
    <p>Origen me ha parecido, con diferencia, <strong>la historia más diluida de la saga de Langdon</strong>. Creo que le ha faltado fuerza y credibilidad.</p>
