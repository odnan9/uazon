<p>Lo primero que he de decir de esta novela es que se trata sin duda de la peor del autor, al menos para mi gusto. No solo <strong>el argumento carece de atractivo</strong> sino también la trama es débil y casi una tomadura de pelo.</p>

<section>
    <h2>Crítica</h2>
    <p>Creo que Dan Brown tiene un problema no resuelto con la religión, ya que prácticamente todas sus novelas tratan de lo mismo: alguien descubre algo que puede poner en duda los dogmas religiosos establecidos. Sin embargo, después de revelarse el misterio nunca pasa nada de lo que todos temen. En relación con esto, llama la atención que el profesor Langdon parece no recordar nunca sus anteriores aventuras y las consecuencias que de ellas se derivan (<a href="#">el final de “Inferno”</a>,  por ejemplo. Quien lo haya leído sabe a qué me refiero…).</p>
    <p>Langdon resetea al final de cada libro, así como su mundo imaginario. Teniendo en cuenta que ese es un personaje de cartón piedra dentro de un bestseller, esto podría casi obviarse, pero llama la atención y pone demasiado al descubierto el carácter artificioso de la narrativa. Casi risibles a estas alturas  las interrupciones de la acción cuando se está a punto de “revelar” el misterio, cosa que siempre se pospone (y, además, algunos personajes se guardan información de manera obvia, burlándose del lector en la cara).</p>
    <p>Destaco la cantidad de escenas sobrantes que hay en la novela, que derivan de subtramas desconectadas entre sí y que bien podrían eliminarse sin afectar casi a la historia.</p>
    <p>Bueno, digo historia por decir, ya que esta es muy tenue y difusa y se podría resumir en: un hombre parecido a Steve Jobs quiere acabar con las religiones con un vídeo, pero un grupo de fanáticos religiosos lo quiere impedir. Y al lado, la intriga palaciega-culebrón de una corona española bastante irreal, sin relación apenas con lo primero y que sirve para meter la parte “ideológica” de la novela. Todo lo demás, secta de clérigos de todas las religiones, secta de ultras católicos del Palmar de Troya, historia del almirante Ávila… sobra. Y no exagero, no, sobra literalmente.</p>
    <p>Las escenas, además, se alargan hasta la extenuación, con la creación y explotación de un único misterio, en localizaciones “emblemáticas” meramente decorativas, pero que se ve a leguas se indican solo para cuando haya que hacer la película y esta quede lucida. </p>
</section>

