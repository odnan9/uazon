        <li>el museo Guggenheim</li>
        <li>el Palacio Real de Madrid</li>
        <li>la Catedral de la Almudena</li>
        <li>el Valle de los Caídos</li>
        <li>el Escorial</li>
        <li>la Sagrada Familia</li>
        <li>la Casa Milá</li>
    </ul>
    <p>Pero el autor no logra sacar el menor partido de estos monumentos. Dejando aparte la descripción del museo Guggenheim, que es algo más “literaria”, el resto se despacha con una introducción al inicio de capítulo que parece extraída de un folleto turístico o de la wikipedia mismamente, auténticos copia-pegas. Por cierto, casi un tercio de la novela se la pasan en el Museo Guggenheim de Bilbao. ¡Increíble! Y más teniendo en cuenta que ahí solo acontece un punto de giro relevante.</p>
</section>

<section>
    <h2>Personajes</h2>
    <p>Los personajes, Langdon incluido, son meros nombres, instrumentos vacíos de alma y contenido, monigotes que hacen cosas sin motivación solo porque el dios Dan así lo quiere. </p>

    <h3>Robert Langdon</h3>
    <p><strong>Ni siquiera les acosa el peligro</strong>; actúan porque sí y del modo más absurdo posible. Supuesto protagonista de la obra... es que aparece poco y carece de personalidad. Dan Brown pone tantos personajes con punto de vista que parece una novela coral.</p>
